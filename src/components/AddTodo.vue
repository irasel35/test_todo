<template>
    <div>
        <form @submit="addTodo">
            <input type="text" v-model="title"
            placeholder="Add Todo..." id="TEXT_FIELD">
            <input type="submit" value="Submit" class="sbmt-btn">
        </form>
    </div>
</template>
<script>
//import uuid from 'uuid'
import {v4} from 'uuid';
export default {
    name:"AddTodo",
    data() {
        return {
            title: ''
        }
    },
    methods: {
        addTodo(e){
            e.preventDefault();
            const newTodo = {
                
                id: v4(),
                title: this.title,
                completed: false

            }
            //send up to parent
            this.$emit('add-todo', newTodo);
            this.title='';

        }
    }
}
</script>
<style scoped>
form{
    display: flex;
}
#TEXT_FIELD {
    flex: 10;
    padding: 5px;
}
.sbmt-btn{
    flex: 2;

}
</style>